from typing import Dict, Any
from cognicore_benchmarks.common.base_evaluator import BaseEvaluator
from cognicore_benchmarks.longmemeval.metrics import evaluate_metrics

class LongMemEvaluator(BaseEvaluator):
    """Evaluates LongMemEval predictions against expected answers."""
    
    def evaluate(self, prediction: str, reference: Dict[str, Any]) -> Dict[str, Any]:
        """
        Evaluate a single LongMemEval prediction.
        
        Args:
            prediction: Text generated by the agent.
            reference: Dictionary containing 'expected_answer' and 'expects_abstain'.
        """
        expected_answer = reference.get('expected_answer', '')
        expects_abstain = reference.get('expects_abstain', False)
        
        return evaluate_metrics(
            prediction=prediction, 
            expected_answer=expected_answer, 
            expects_abstain=expects_abstain
        )
